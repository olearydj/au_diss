def extract_comments(md_file_path):
    """
    Extracts the h2 elements and underlying comments from hand-transcribed
    markdown notes (i.e., 1???.md). Ensures all notes are prefixed with "- ",
    and all h2 elements are preceded by one blank line.

    Args:
        md_file_path (Path): Path to the markdown file to extract comments from.

    Returns:
        md_out (list[str]): Processed lines.
    """
    logger.debug(f"    > Extracting comments from: {md_file_path}")

    with open(md_file_path, "r") as md_input:
        md_in = md_input.readlines()

    h2 = False
    md_out = []

    for line in md_in:
        if line.startswith("##"):
            h2 = True
        elif line.isspace():
            h2 = False
        if h2:
            if not line.startswith(("- ", "##")):
                # fix notes that aren't bulleted
                line = "- " + line
            if line.startswith("##"):
                line = "\n#" + line
            md_out.append(line)
            # print(line, end="")

    # rprint(md_out)
    return md_out


def compile_handwritten_feedback(notes: list[Path]):
    # for each trial report, extract script comments and feedback from hand-transcribed notes
    trial_notes = {}
    for note in notes:
        # e.g. if report = /Users/djo/dev/au/au_diss/reports/1001.md
        report_num = note.stem  # '1001'

        trial_notes[report_num] = extract_comments(note)
    return trial_notes


def compile_csv_data(p_data, participant):
    # build event_data from event subclips for each participant
    rows = []
    for phase_num in PHASES.keys():
        phase_name = PHASES[phase_num]
        events = p_data[phase_name]["events"]

        for event in events:
            e_name = event["title"]
            e_start = round(event["timestamp"], 3)
            e_dur = round(event["duration"], 3)
            e_desc = event["description"]
            row = [int(participant), phase_num, e_name, e_start, e_dur, e_desc]
            rows.append(row)

    return rows


